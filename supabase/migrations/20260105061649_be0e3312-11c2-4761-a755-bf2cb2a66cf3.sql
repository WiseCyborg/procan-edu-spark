## 1. Why Dosing Matters

Cannabis affects everyone differently. The right dose for one person may be too much or too little for another.

### Consequences of Wrong Dosing

**Too little:**
- No noticeable effects
- Wasted product/money

**Too much:**
- Anxiety, paranoia
- Nausea, dizziness
- Impaired coordination
- Unpleasant experience that can last hours

> **Key Principle:** It''s always better to underdose and take more than to overconsume and wait it out.

---

## 2. The Golden Rule: Start Low, Go Slow

### For Inhalation (Smoking/Vaping)

1. Take one small puff
2. Wait 10-15 minutes
3. Assess how you feel
4. Take another puff only if needed
5. Repeat as necessary

### For Edibles

1. Start with 2.5-5mg THC
2. Wait at least 2 hours before more
3. Effects may take 30-90 minutes to begin
4. Peak effects occur 2-3 hours after consumption
5. Don''t stack doses too quickly

### For Tinctures

1. Start with a low dose (5-10mg)
2. Hold under tongue for 30-60 seconds
3. Wait 30-45 minutes to assess
4. Increase gradually over sessions

---

## 3. Factors That Affect Your Dose

### Individual Factors

- **Body weight** – Larger people may need more
- **Metabolism** – Fast metabolizers process THC quickly
- **Tolerance** – Regular users need more
- **Genetics** – Some people are more sensitive to THC
- **Empty vs. full stomach** – Edibles hit harder on empty stomach

### Product Factors

- **THC percentage** – Higher % = more potent
- **CBD content** – May moderate THC effects
- **Terpenes** – Can enhance or mellow effects
- **Product type** – Edibles are metabolized differently

### Environmental Factors

- **Set and setting** – Mood and environment matter
- **Stress levels** – Anxiety can intensify THC effects
- **Experience** – First-timers should start very low

---

## 4. Dosing Guidelines by Method

### Smoking/Vaping Flower

| Experience | Starting Dose |
|---|---|
| New user | 1 small puff, wait 15 min |
| Occasional | 2-3 puffs |
| Regular | Dose to effect |

### Edibles (THC mg)

| Experience | Starting Dose |
|---|---|
| New user | 2.5mg |
| Occasional | 5-10mg |
| Regular | 10-25mg |
| High tolerance | 25-50mg+ |

> **Maryland Edible Standard:** Most Maryland edibles come in 10mg per piece with 100mg per package.

### Concentrates

| Experience | Starting Dose |
|---|---|
| New to concentrates | Rice grain size |
| Experienced | Small dab, dose up |

> **Warning:** Concentrates can be 60-90% THC. Even experienced flower users should start small.

---

## 5. The Biphasic Effect

Cannabis often has **biphasic effects** – meaning low and high doses produce opposite results.

### Examples

| Cannabinoid | Low Dose | High Dose |
|---|---|---|
| THC | Relaxation, euphoria | Anxiety, paranoia |
| CBD | Alertness | Sedation |
| THCV | Appetite suppression | May stimulate appetite |

> **Takeaway:** More is not always better. Finding your "sweet spot" takes experimentation.

---

## 6. What If You Consume Too Much?

### Symptoms of Overconsumption

- Anxiety or panic
- Rapid heartbeat
- Dizziness
- Nausea
- Paranoia
- Confusion

### What to Do

1. **Stay calm** – Remind yourself it will pass
2. **Find a safe space** – Lie down somewhere comfortable
3. **Hydrate** – Water or juice (avoid caffeine/alcohol)
4. **Try CBD** – May help reduce intensity
5. **Black pepper** – Some report sniffing/chewing helps
6. **Distraction** – Calming music, TV, conversation
7. **Wait it out** – Effects will fade within hours

> **Remember:** No one has ever died from a cannabis overdose. It will pass.

### Edible-Specific Note

Edible overconsumption can last 6-8+ hours. The best approach is prevention through careful dosing.

---

## 7. Building Tolerance

### What Is Tolerance?

Regular cannabis use leads to **tolerance** – needing more to achieve the same effects.

### Managing Tolerance

- **Tolerance breaks (T-breaks):** Abstain for days or weeks
- **Dose cycling:** Vary your doses
- **Strain rotation:** Switch strains regularly
- **Consumption breaks:** Skip days between sessions

> **Tip:** A 48-72 hour break can noticeably reduce tolerance.

---

## 8. Keeping a Cannabis Journal

Track your experiences to find your ideal dose:

### What to Record

- Date and time
- Product name and type
- Dose (mg or puffs)
- Method (smoke, vape, edible)
- Time to onset
- Effects (physical and mental)
- Duration
- Overall rating

