## 1. Why Quality Matters

Cannabis quality affects:

- **Safety** – Contaminants can cause harm
- **Effectiveness** – Accurate potency ensures proper dosing
- **Experience** – Fresh, well-cured cannabis tastes better
- **Value** – Quality products provide consistent results

> **Key Point:** Maryland requires all cannabis products to pass lab testing before sale.

---

## 2. Maryland Testing Requirements

### What Gets Tested

All cannabis products sold in Maryland dispensaries must be tested for:

#### Potency
- THC percentage
- CBD percentage
- Other cannabinoids
- Terpene profiles (some products)

#### Contaminants
- **Pesticides** – Harmful chemicals
- **Heavy metals** – Lead, mercury, arsenic
- **Microbials** – Mold, bacteria, yeast
- **Residual solvents** – From extraction processes
- **Mycotoxins** – Toxins from fungal growth

### Who Does the Testing

- Independent, MMCC-licensed labs
- Testing must occur before products reach dispensaries
- Failed batches cannot be sold

---

## 3. Reading Cannabis Labels

Maryland requires specific information on all cannabis products:

### Required Label Elements

1. **Product name and type**
2. **Net weight/volume**
3. **THC and CBD content** (mg or %)
4. **Batch/lot number**
5. **Harvest/manufacture date**
6. **Expiration date**
7. **License numbers** (grower/processor)
8. **Warning statements**
9. **Dosing information** (for edibles)

### Understanding THC Content

**For Flower:**
- Listed as percentage (e.g., 22% THC)
- Higher % = more potent

**For Edibles:**
- Listed in milligrams (e.g., 10mg THC per serving)
- Package shows total mg and servings

**For Concentrates:**
- Listed as percentage (often 60-90%+)

---

## 4. Quality Indicators

### Signs of Quality Cannabis (Flower)

✅ **Good Signs:**
- Vibrant green color (with purple, orange, or other hues)
- Visible trichomes (crystals)
- Strong, fresh aroma
- Dense but not rock-hard buds
- Properly trimmed
- Slightly sticky texture

❌ **Warning Signs:**
- Brown, yellow, or faded color
- No visible trichomes
- Hay-like or musty smell
- Crumbly, dry texture
- Visible mold or seeds
- Excessive stems

### Signs of Quality Edibles

- Proper packaging with child-resistant features
- Clear dosing information
- Intact seal
- Within expiration date

### Signs of Quality Concentrates

- Clear packaging with lab results
- Appropriate consistency for type
- No unusual colors or residues
- Fresh aroma

---

## 5. Safe Storage at Home

### General Storage Principles

- **Cool:** Room temperature or below (60-70°F ideal)
- **Dark:** Protect from light (UV degrades THC)
- **Dry:** Humidity around 55-62%
- **Airtight:** Prevent oxidation and odor escape

### Flower Storage

- **Best:** Glass jars with airtight seals
- **OK:** Original dispensary container
- **Avoid:** Plastic bags (static damages trichomes)

### Edible Storage

- Keep in original child-resistant packaging
- Store in cool, dry place
- Refrigerate if indicated on package
- Note expiration dates

### Concentrate Storage

- Keep in original container
- Store in cool environment
- Some concentrates may need refrigeration

---

## 6. Child and Pet Safety

### Critical Safety Measures

- **Child-resistant containers:** Always use them
- **Locked storage:** Consider a lockbox
- **High placement:** Store out of reach
- **Edible caution:** They often look like candy
- **Education:** Teach children that these are not food/toys

### If Accidental Ingestion Occurs

**For children:**
1. Stay calm
2. Call Poison Control: 1-800-222-1222
3. Note what was consumed and how much
4. Seek medical attention if recommended

**For pets:**
1. Contact veterinarian immediately
2. Note product and amount
3. THC is more toxic to pets than humans

---

## 7. Responsible Consumption

### Safety Guidelines

- **Never drive** while impaired
- **Don''t mix** with alcohol or other substances (unexpectedly)
- **Know your limits** – Start low, go slow
- **Consume in safe environments**
- **Have a sober companion** when trying new products
- **Stay hydrated**

### Who Should Avoid Cannabis

- Pregnant or breastfeeding individuals
- Those with history of psychosis
- People on certain medications (consult doctor)
- Anyone under legal age (21 for adult-use, 18 for medical)

---

## 8. Reporting Quality Issues

### If You Suspect a Product Problem

1. **Stop using** the product
2. **Document** the issue (photos, notes)
3. **Contact** the dispensary
4. **File a complaint** with MMCC if needed

### What to Report

- Mold or contamination
- Incorrect labeling
- Adverse reactions
- Product defects

