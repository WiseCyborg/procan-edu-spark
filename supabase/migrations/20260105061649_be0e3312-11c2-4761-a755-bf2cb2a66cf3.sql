## 1. Overview of Consumption Methods

Cannabis can be consumed in many ways, each with unique characteristics:

| Method | Onset | Duration | Intensity |
|---|---|---|---|
| Smoking | 5-15 min | 2-4 hours | Moderate-High |
| Vaping | 5-10 min | 1-3 hours | Moderate-High |
| Edibles | 30-90 min | 4-8 hours | Can be intense |
| Tinctures | 15-45 min | 4-6 hours | Moderate |
| Topicals | 15-30 min | 2-4 hours | Localized only |
| Concentrates | Immediate | 1-3 hours | Very High |

---

## 2. Inhalation Methods

### Smoking (Flower)

**How it works:** Dried cannabis flower is burned and inhaled.

**Formats:**
- Joints (rolled in paper)
- Pipes and bowls
- Bongs (water filtration)
- Blunts (tobacco wrap)

**Pros:**
- Fast onset (5-15 minutes)
- Easy to control dose
- Traditional experience
- Wide variety of strains

**Cons:**
- Combustion creates harmful byproducts
- Distinctive smell
- May irritate lungs
- Requires supplies (papers, lighter)

---

### Vaping

**How it works:** Cannabis is heated to release vapor without combustion.

**Formats:**
- Cartridges (pre-filled, disposable)
- Pods (brand-specific systems)
- Dry herb vaporizers
- Concentrate pens

**Pros:**
- Cleaner than smoking (no combustion)
- More discreet (less smell)
- Flavor preservation
- Portable and convenient

**Cons:**
- Battery required
- Cartridge quality varies
- Concerns about additives
- Equipment cost

> **Safety Note:** Only purchase vape products from licensed Maryland dispensaries. Illicit market cartridges have been linked to serious lung injuries.

---

## 3. Oral Consumption

### Edibles

**How it works:** Cannabis-infused food or beverages are digested.

**Formats:**
- Gummies and chocolates
- Baked goods
- Beverages
- Capsules

**Pros:**
- Long-lasting effects (4-8 hours)
- No lung exposure
- Discreet
- Precise dosing (if labeled accurately)

**Cons:**
- Delayed onset (30-90 minutes or more)
- Easy to overconsume
- Effects can be intense
- Harder to dose accurately

> **Golden Rule for Edibles:** Start low (2.5-5mg THC), wait at least 2 hours before taking more.

---

### Tinctures

**How it works:** Liquid cannabis extract taken under the tongue (sublingual).

**Formats:**
- Oil-based tinctures
- Alcohol-based tinctures
- Nano-emulsified (faster absorption)

**Pros:**
- Faster than edibles (15-45 minutes)
- Easy to dose with dropper
- Discreet
- No smoking required

**Cons:**
- Taste can be strong
- Effects vary by product
- More expensive than flower

---

## 4. Topical Application

### How it works

Cannabis-infused products applied directly to skin.

**Formats:**
- Creams and lotions
- Balms and salves
- Transdermal patches
- Bath products

**Pros:**
- Localized relief (joint pain, muscle soreness)
- Non-intoxicating (except transdermal patches)
- No systemic effects
- Easy to use

**Cons:**
- Won''t produce a "high" (most topicals)
- Effects limited to application area
- May not work for everyone

---

## 5. Concentrates and Dabbing

### How it works

Highly potent cannabis extracts are vaporized on a hot surface.

**Formats:**
- Wax, shatter, budder
- Live resin, rosin
- Diamonds and sauce
- RSO (Rick Simpson Oil)

**Pros:**
- Very potent (60-90%+ THC)
- Fast onset
- Strong effects for high-tolerance users
- Flavorful (especially live resin)

**Cons:**
- Not for beginners
- Requires special equipment (dab rig, torch)
- Easy to overconsume
- High tolerance development

> **Warning:** Concentrates are extremely potent. Start with a tiny amount if trying for the first time.

---

## 6. Choosing the Right Method

### Consider Your Goals

| If You Want... | Consider... |
|---|---|
| Fast relief | Smoking, vaping, tinctures |
| Long-lasting effects | Edibles |
| Discretion | Edibles, tinctures, vapes |
| No inhalation | Edibles, tinctures, topicals |
| Localized pain relief | Topicals |
| Maximum potency | Concentrates |

### Consider Your Experience

- **Beginners:** Start with low-dose edibles (2.5-5mg) or one puff of flower
- **Intermediate:** Vapes, tinctures, moderate edibles
- **Experienced:** Concentrates, higher-dose options

---

## 7. Consumption Safety

### Universal Guidelines

- **Start low, go slow** – especially with edibles
- **Wait before re-dosing** – edibles can take 2+ hours
- **Know your source** – buy from licensed dispensaries
- **Never drive impaired** – cannabis affects reaction time
- **Store safely** – keep away from children and pets

