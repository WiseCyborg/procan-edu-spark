## 1. Maryland''s Cannabis Programs

Maryland has two legal cannabis programs:

### Medical Cannabis Program
- Began: 2017
- Requires: Patient registration with MMCC
- Age: 18+ (minors with caregiver)
- Benefits: Tax advantages, higher limits, strain access

### Adult-Use Program
- Began: July 1, 2023
- Requires: Valid ID (21+)
- No registration needed
- Standard tax rate applies

---

## 2. The Maryland Medical Cannabis Commission (MMCC)

The MMCC oversees all cannabis activities in Maryland:

- **Licenses** dispensaries, growers, processors
- **Registers** patients, caregivers, providers
- **Regulates** product testing and safety
- **Enforces** compliance requirements

Website: mmcc.maryland.gov

---

## 3. Qualifying Conditions

Maryland does not have a strict list of qualifying conditions. Instead, a **certifying provider** determines if cannabis may benefit you.

### Commonly Certified Conditions

- Chronic pain
- PTSD
- Anxiety and depression
- Insomnia
- Cancer and chemotherapy side effects
- Multiple sclerosis
- Epilepsy and seizure disorders
- Crohn''s disease
- Glaucoma
- HIV/AIDS
- Severe nausea
- Muscle spasms

> **Note:** Any condition your provider believes may benefit from cannabis can qualify you.

---

## 4. Becoming a Medical Patient

### Step 1: Provider Certification
- Visit an MMCC-registered certifying provider
- Provider evaluates your condition
- If approved, provider certifies you in the system

### Step 2: MMCC Registration
- Create account at mmcc.maryland.gov
- Complete registration with provider certification
- Pay $50 registration fee (waived for some groups)
- Receive your MMCC ID

### Step 3: Visit a Dispensary
- Bring your MMCC ID and government ID
- Consult with a dispensary agent
- Purchase products

---

## 5. Medical vs. Adult-Use: Key Differences

| Feature | Medical | Adult-Use |
|---|---|---|
| Age requirement | 18+ | 21+ |
| Registration | Required (MMCC) | Not required |
| Purchase limits | Higher limits | Standard limits |
| Tax rate | Lower (6%) | Higher (9%) |
| Product access | May have exclusive strains | Standard menu |
| Caregivers | Allowed | Not applicable |

---

## 6. Caregivers

### What Is a Caregiver?
A person registered to purchase and administer cannabis on behalf of a patient.

### Who Can Be a Caregiver?
- Must be 21+
- Must register with MMCC
- Background check required
- Designated by the patient

### Caregiver Responsibilities
- Purchase cannabis for the patient
- Assist with administration
- Ensure safe storage
- Cannot consume patient''s cannabis

---

## 7. Patient Rights and Protections

### Maryland Law Protects Patients

- **Employment:** Some protections (but not absolute)
- **Housing:** Landlords may still prohibit
- **Child custody:** Medical use is not automatic disqualification
- **Federal law:** Cannabis remains illegal federally

### Things to Know

- **DUI laws still apply** – No driving while impaired
- **Federal property** – No cannabis on federal land
- **Other states** – Maryland cards may not be accepted elsewhere
- **Gun purchases** – Federal forms ask about cannabis use

