## Overview
Dispensary employees must possess comprehensive knowledge of cannabis products to provide safe, effective patient education. Understanding product categories, cannabinoid profiles, consumption methods, and safety protocols enables you to help patients make informed decisions about their medicine. This module covers essential product knowledge and safety standards required for responsible dispensary operations.

## Cannabis Product Categories

### Flower (Dried Cannabis)
Flower represents the traditional form of cannabis and remains highly popular with patients. Key characteristics:
- **THC Range**: 10-30% typically
- **Onset**: 5-10 minutes when smoked/vaporized
- **Duration**: 2-4 hours
- **Forms**: Pre-packaged eighths (3.5g), quarters (7g), half-ounces (14g)
- **Storage**: Air-tight containers, cool/dark location, humidity control packs

### Concentrates
Concentrated cannabis extracts offer higher potency and diverse consumption options:
- **Types**: Wax, shatter, crumble, live resin, rosin, distillate
- **THC Range**: 60-95%
- **Consumption**: Vaporization, dabbing, adding to flower
- **Cautions**: High potency requires careful dosing, not recommended for new patients

### Edibles
Food products infused with cannabis provide long-lasting, discreet medicine:
- **THC Range**: 5-100mg per package (10mg serving standard)
- **Onset**: 30-90 minutes (delayed due to digestion)
- **Duration**: 4-8 hours (sometimes longer)
- **Forms**: Gummies, chocolates, baked goods, mints, beverages
- **Critical Warning**: Delayed onset causes accidental overconsumption—"Start low, go slow"

### Tinctures and Oils
Liquid cannabis extracts offer precise dosing and sublingual absorption:
- **Administration**: Sublingual (under tongue) or mixed with food/beverage
- **Onset**: 15-30 minutes sublingual, 30-90 minutes oral
- **Duration**: 4-6 hours
- **Advantages**: Precise dosing with dropper, easy titration, no smoking

### Topicals
Non-intoxicating cannabis products applied to skin:
- **Uses**: Localized pain, inflammation, skin conditions
- **Forms**: Lotions, balms, patches, bath products
- **Key Point**: Topicals do not produce psychoactive effects (do not enter bloodstream)

## Cannabinoid and Terpene Profiles

### Major Cannabinoids
**THC (Tetrahydrocannabinol)**
- Primary psychoactive compound
- Effects: Pain relief, appetite stimulation, sleep aid
- Considerations: Can cause anxiety in high doses

**CBD (Cannabidiol)**
- Non-intoxicating
- Effects: Anti-inflammatory, anti-anxiety, seizure control
- Often used to balance THC effects

**CBG, CBN, CBC**
- Minor cannabinoids with unique therapeutic properties
- Increasing research and product development

### Common Terpenes
Terpenes are aromatic compounds that contribute to effects and flavors:
- **Myrcene**: Sedating, muscle relaxant (mango aroma)
- **Limonene**: Uplifting, stress relief (citrus aroma)
- **Pinene**: Alertness, memory (pine aroma)
- **Linalool**: Calming, anti-anxiety (lavender aroma)
- **Caryophyllene**: Anti-inflammatory, pain relief (pepper aroma)

## Product Safety and Quality Assurance

### Certificate of Analysis (COA) Requirements
Every product must have laboratory testing results verifying:
- **Potency**: Accurate THC/CBD percentages
- **Contaminants**: Pesticides, heavy metals, residual solvents
- **Microbials**: Mold, bacteria, E. coli, salmonella
- **Mycotoxins**: Toxic compounds from mold
Employees must verify COA presence and product matching before sale.

### Identifying Unsafe Products
Red flags requiring product removal:
- Mold visible on flower (white, grey, or black spots)
- Off odors (ammonia, hay, must)
- Broken seals or tampered packaging
- Missing or expired COAs
- Discrepancies between label and actual product
- Customer reports of adverse reactions

### Storage Requirements
Proper storage maintains product safety and quality:
- **Temperature**: 60-70°F, avoid temperature fluctuations
- **Humidity**: 55-65% RH for flower
- **Light**: Dark storage, UV protection
- **Security**: Locked, limited access areas
- **Organization**: FIFO (First In, First Out) rotation

## Patient Education Best Practices

### New Patient Consultations
When working with new patients:
1. Assess experience level and expectations
2. Explain differences between product types
3. Emphasize "start low, go slow" approach
4. Recommend lower-potency products initially
5. Explain onset times and duration
6. Provide written dosing guidance

### Addressing Safety Concerns
Common patient questions:
- **"Can I drive after using cannabis?"** No, impairment affects reaction time
- **"Can I overdose?"** Not lethal, but overconsumption causes severe discomfort
- **"Will it interact with my medications?"** Possible—consult physician about specific medications
- **"How do I store products safely?"** Child-resistant containers, locked storage, away from pets

## Conclusion
Comprehensive product knowledge enables you to serve as a trusted advisor to patients navigating medical cannabis options. Understanding product categories, cannabinoid profiles, safety standards, and proper storage ensures patients receive safe, effective medicine. Always prioritize patient safety over sales, and never hesitate to refer complex medical questions to healthcare providers.' 
