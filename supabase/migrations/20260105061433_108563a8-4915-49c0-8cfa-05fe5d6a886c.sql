## 1. What Is Cannabis?

Cannabis is a flowering plant that has been used by humans for thousands of years for:

- **Medicine** – pain relief, relaxation, appetite
- **Fiber** – hemp rope, textiles, paper
- **Recreation** – its psychoactive effects
- **Spiritual practices** – in various cultures worldwide

The cannabis plant produces over **100 different cannabinoids** and hundreds of other compounds including terpenes and flavonoids.

---

## 2. Parts of the Cannabis Plant

### Flowers (Buds)
- The most potent part of the plant
- Where cannabinoids and terpenes are concentrated
- What you typically see sold in dispensaries

### Leaves
- Fan leaves (large) contain minimal cannabinoids
- Sugar leaves (small, near flowers) contain more resin

### Seeds
- Used for cultivation and hemp seed products
- Contain no cannabinoids

### Stems and Stalks
- Used for fiber (hemp)
- Not consumed for effects

---

## 3. Cannabis vs. Hemp

Both are the same plant species (*Cannabis sativa*), but they are legally distinct:

| | Cannabis | Hemp |
|---|---|---|
| **THC Content** | More than 0.3% | Less than 0.3% |
| **Psychoactive** | Yes | No |
| **Legal Status** | State-regulated | Federally legal (2018 Farm Bill) |
| **Primary Use** | Medical/Adult-use | Industrial, CBD products |

> **Key Point:** Hemp-derived CBD products are widely available, but cannabis products with THC require purchase from a licensed dispensary.

---

## 4. Major Cannabinoids

### THC (Tetrahydrocannabinol)
- Primary psychoactive compound
- Produces the "high" effect
- Used for pain, nausea, appetite, sleep

### CBD (Cannabidiol)
- Non-intoxicating
- Often used for anxiety, inflammation, seizures
- Does not produce a "high"

### Other Cannabinoids
- **CBG** – The "mother cannabinoid"
- **CBN** – Associated with sedation
- **THCV** – May suppress appetite

---

## 5. Brief History of Cannabis

### Ancient Use (Pre-1900s)
- Used in China, India, Middle East for medicine
- Hemp was a major agricultural crop in colonial America

### 20th Century Prohibition
- 1937: Marihuana Tax Act restricted cannabis in the US
- 1970: Controlled Substances Act classified cannabis as Schedule I
- Cannabis became illegal in most countries

### Modern Legalization
- 1996: California becomes first state to legalize medical cannabis
- 2012: Colorado and Washington legalize adult-use
- 2023: Maryland legalizes adult-use cannabis

