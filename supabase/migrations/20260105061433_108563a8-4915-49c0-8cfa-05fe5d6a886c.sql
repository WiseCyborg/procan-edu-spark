## 1. What Is CBD?

**CBD (Cannabidiol)** is the second most abundant cannabinoid in cannabis. Unlike THC, CBD does not produce intoxicating effects.

### Key Characteristics

- **Non-psychoactive** – No "high" feeling
- **Widely available** – Hemp-derived CBD is legal federally
- **Multiple forms** – Oils, capsules, topicals, edibles
- **Growing research** – FDA approved for certain epilepsy (Epidiolex)

---

## 2. How CBD Works

CBD interacts with the endocannabinoid system differently than THC:

### CB1 and CB2 Receptors
- CBD does **not** directly bind to CB1 receptors (unlike THC)
- It modulates how other compounds interact with receptors
- May influence serotonin receptors (5-HT1A)

### The "Entourage Effect"
- CBD may modify how THC affects you
- Some users report CBD reduces THC-related anxiety
- Full-spectrum products contain CBD, THC, and other compounds together

> **Note:** CBD does not "cancel out" THC, but may influence the experience.

---

## 3. Potential Benefits of CBD

Research and user reports suggest CBD may help with:

### Anxiety and Stress
- Some studies show anxiolytic (anti-anxiety) effects
- May promote calm without sedation

### Pain and Inflammation
- Topical CBD for localized pain
- May have anti-inflammatory properties

### Sleep
- Some users report improved sleep quality
- May work better combined with CBN

### Seizures
- FDA-approved Epidiolex for rare epilepsy forms
- Strongest clinical evidence

### Other Areas (Ongoing Research)
- Neuroprotection
- Skin conditions (acne, eczema)
- Addiction recovery support

> **Important:** CBD is not FDA-approved for most conditions. Research is promising but still developing.

---

## 4. CBD Product Types

### CBD Oils and Tinctures
- Taken under the tongue (sublingual)
- Fast absorption (15–30 minutes)
- Easy dose control with dropper

### Capsules and Softgels
- Pre-measured doses
- Convenient and discreet
- Slower onset (30–60 minutes)

### Edibles and Beverages
- Gummies, chocolates, drinks
- Tasty but variable absorption
- Effects may take longer

### Topicals
- Creams, balms, lotions
- Applied directly to skin
- For localized relief, not systemic effects

### Vape Products
- Fast onset (minutes)
- Concerns about lung health with vaping

---

## 5. CBD Spectrum Types

### Full-Spectrum CBD
- Contains CBD + other cannabinoids + THC (under 0.3%)
- May provide entourage effect
- Could trigger positive drug test

### Broad-Spectrum CBD
- Contains CBD + other cannabinoids
- THC removed
- Entourage effect without THC

### CBD Isolate
- Pure CBD only (99%+)
- No other cannabinoids or THC
- Good for those avoiding all THC

---

## 6. Evaluating CBD Quality

### Look For:
✅ **Certificate of Analysis (COA)** – Third-party lab testing  
✅ **Clear labeling** – CBD content per serving  
✅ **Source information** – Where hemp was grown  
✅ **Extraction method** – CO2 extraction is preferred  

### Red Flags:
❌ No lab testing available  
❌ Unrealistic health claims ("cures cancer")  
❌ No dosage information  
❌ Extremely cheap pricing  

> **Tip:** Always buy CBD from licensed dispensaries or reputable brands with transparent testing.

---

## 7. CBD Dosing Guidelines

There is no universal CBD dose. Start low and increase gradually.

### General Starting Points:
- **Low:** 10–20mg per day
- **Medium:** 25–50mg per day
- **High:** 50–100mg+ per day (consult healthcare provider)

### Factors That Affect Dosing:
- Body weight
- Condition being addressed
- Individual metabolism
- Product type and bioavailability

