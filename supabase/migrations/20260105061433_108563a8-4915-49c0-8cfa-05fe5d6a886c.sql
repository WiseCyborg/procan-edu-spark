- **THC %** – Psychoactive potency
- **CBD %** – Non-intoxicating cannabinoid
- **Total cannabinoids** – All cannabinoids combined
- **Terpene profile** – Aromatic compounds

### Sample Label Reading

A product showing:
- THC: 18%
- CBD: 1%
- CBG: 0.5%
- Myrcene: 1.2%

This tells you it''s THC-dominant with a small CBD/CBG presence and myrcene (relaxing terpene).

