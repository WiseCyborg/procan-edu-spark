## 1. Beyond THC and CBD

Cannabis contains over **100 different cannabinoids**. While THC and CBD are most abundant, minor cannabinoids are gaining attention for their unique properties.

### Why Minor Cannabinoids Matter

- Each cannabinoid has distinct effects
- They work together synergistically
- Products can be formulated for specific outcomes
- Understanding them helps you choose products

---

## 2. CBG (Cannabigerol)

### The "Mother Cannabinoid"
- CBGA is the precursor to THC, CBD, and CBC
- Found in higher amounts in young cannabis plants
- Non-intoxicating

### Potential Benefits
- Antibacterial properties
- May reduce inflammation
- Possible neuroprotective effects
- Eye health (glaucoma research)

### Products
- CBG oils and tinctures
- CBG-dominant flower strains
- Often combined with CBD

---

## 3. CBN (Cannabinol)

### The "Sleepy Cannabinoid"
- Forms when THC ages and oxidizes
- Mildly psychoactive
- Associated with sedation

### Potential Benefits
- Sleep support
- Pain relief
- Anti-inflammatory
- Appetite stimulation

### Products
- CBN sleep gummies
- CBN tinctures (often with CBD or melatonin)
- Aged cannabis flower

> **Note:** CBN research is still limited. Effects may be enhanced when combined with THC and other cannabinoids.

---

## 4. THCV (Tetrahydrocannabivarin)

### The "Diet Weed" Cannabinoid
- Structurally similar to THC
- At low doses: may block CB1 receptors
- At high doses: may activate CB1 (mild high)

### Potential Benefits
- Appetite suppression (opposite of THC)
- Energy and focus
- Blood sugar regulation (diabetes research)
- Shorter duration than THC

### Products
- THCV-specific products (less common)
- African sativa strains (Durban Poison)
- Often marketed for daytime use

---

## 5. Other Notable Cannabinoids

### CBC (Cannabichromene)
- Non-intoxicating
- May have anti-inflammatory and mood effects
- Works well with other cannabinoids

### Delta-8 THC
- Less potent than Delta-9 THC
- Often hemp-derived
- Milder psychoactive effects
- Legal status varies

### THCA (Tetrahydrocannabinolic Acid)
- Non-psychoactive precursor to THC
- Found in raw cannabis
- Converts to THC when heated
- Some consume raw cannabis for THCA

### CBDA (Cannabidiolic Acid)
- Non-psychoactive precursor to CBD
- Found in raw cannabis
- Research on nausea and inflammation

---

## 6. The Entourage Effect

### What Is It?
The theory that cannabis compounds work better together than isolated.

### How It Works

- **Cannabinoids + Cannabinoids:** CBD may moderate THC effects
- **Cannabinoids + Terpenes:** Myrcene may enhance THC absorption
- **Whole-plant synergy:** Full-spectrum may outperform isolates

### Product Implications

| Product Type | Entourage Effect? |
|---|---|
| Full-spectrum | Yes (all compounds) |
| Broad-spectrum | Partial (no THC) |
| Isolate | No (single compound) |

> **For consumers:** If you want the entourage effect, choose full-spectrum products.

---

## 7. Reading Cannabinoid Profiles

