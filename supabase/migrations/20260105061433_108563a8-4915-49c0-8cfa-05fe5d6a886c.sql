## 1. What Are Terpenes?

**Terpenes** are aromatic compounds found in many plants, including cannabis. They are responsible for the distinct smells and flavors of different cannabis strains.

### Terpenes in Nature

- **Lavender:** Linalool (calming)
- **Citrus fruits:** Limonene (uplifting)
- **Pine trees:** Pinene (alertness)
- **Mangoes:** Myrcene (relaxing)

> **Key Point:** Terpenes are not unique to cannabis. You encounter them daily in fruits, herbs, and flowers.

---

## 2. Why Terpenes Matter

### Beyond Smell and Taste

Terpenes may influence:

- **Effects** – Some terpenes are relaxing, others energizing
- **Entourage effect** – They work synergistically with cannabinoids
- **Therapeutic potential** – Anti-inflammatory, anti-anxiety properties

### Indica vs. Sativa?

The traditional indica/sativa distinction is less useful than understanding terpene profiles:

- "Indica" effects often correlate with **myrcene-dominant** strains
- "Sativa" effects often correlate with **limonene or pinene-dominant** strains

---

## 3. Common Cannabis Terpenes

### Myrcene 🥭
- **Smell:** Earthy, musky, fruity (mango)
- **Effects:** Relaxing, sedating, "couch-lock"
- **Also found in:** Mangoes, hops, thyme
- **Strains:** OG Kush, Blue Dream, Granddaddy Purple

### Limonene 🍋
- **Smell:** Citrus, lemon, orange
- **Effects:** Uplifting, mood-enhancing, stress relief
- **Also found in:** Citrus peels, juniper
- **Strains:** Super Lemon Haze, Durban Poison, Wedding Cake

### Pinene 🌲
- **Smell:** Pine, fresh forest
- **Effects:** Alertness, focus, may counter THC memory effects
- **Also found in:** Pine needles, rosemary, basil
- **Strains:** Jack Herer, Blue Dream, Snoop''s Dream

### Linalool 💜
- **Smell:** Floral, lavender, sweet
- **Effects:** Calming, anti-anxiety, sleep support
- **Also found in:** Lavender, mint, cinnamon
- **Strains:** Lavender, Do-Si-Dos, Amnesia Haze

### Caryophyllene 🌶️
- **Smell:** Spicy, peppery, woody
- **Effects:** Anti-inflammatory, pain relief
- **Unique:** Only terpene that binds to CB2 receptors
- **Also found in:** Black pepper, cloves, cinnamon
- **Strains:** GSC (Girl Scout Cookies), Bubba Kush, Chemdawg

### Humulene 🍺
- **Smell:** Earthy, woody, hoppy
- **Effects:** Appetite suppressant, anti-inflammatory
- **Also found in:** Hops, coriander, basil
- **Strains:** White Widow, Headband, Sour Diesel

### Terpinolene 🌸
- **Smell:** Floral, herbal, slightly citrus
- **Effects:** Uplifting, may be sedating in high amounts
- **Also found in:** Nutmeg, tea tree, apples
- **Strains:** Jack Herer, Ghost Train Haze, Dutch Treat

---

## 4. Terpenes and Effects Guide

| If You Want... | Look For These Terpenes |
|---|---|
| Relaxation/Sleep | Myrcene, Linalool |
| Energy/Focus | Limonene, Pinene, Terpinolene |
| Pain Relief | Caryophyllene, Myrcene |
