## 1. What Is THC?

**THC (Delta-9-Tetrahydrocannabinol)** is the primary psychoactive compound in cannabis. It is responsible for the "high" associated with cannabis use.

### How THC Works

1. You consume cannabis (smoke, vape, eat, etc.)
2. THC enters your bloodstream
3. THC binds to **CB1 receptors** in your brain
4. This triggers changes in mood, perception, and cognition

> **Key Point:** THC mimics a natural brain chemical called anandamide, which is part of your endocannabinoid system.

---

## 2. Forms of THC

### Delta-9 THC
- The "classic" THC
- Most abundant in cannabis
- Produces intoxicating effects
- Regulated in Maryland dispensaries

### Delta-8 THC
- Less potent than Delta-9
- Often derived from hemp
- Legal status varies by state
- May produce milder effects

### THCA (THC Acid)
- Non-psychoactive precursor to THC
- Found in raw cannabis
- Converts to THC when heated (decarboxylation)
- Some use raw cannabis for THCA benefits

---

## 3. Effects of THC

### Common Effects

**Positive:**
- Euphoria and relaxation
- Enhanced sensory experiences
- Increased appetite ("munchies")
- Pain relief
- Creativity (for some)

**Potential Negative:**
- Anxiety or paranoia
- Impaired short-term memory
- Slower reaction time
- Increased heart rate
- Dry mouth and red eyes

> **Important:** Effects vary greatly between individuals. What feels relaxing to one person may cause anxiety in another.

---

## 4. Factors That Influence THC Effects

### Dose
- More THC = stronger effects
- Always start low (2.5–5mg for edibles)

### Tolerance
- Regular users need more for same effect
- Tolerance develops quickly

### Consumption Method
- **Smoking/Vaping:** Onset in 5–15 minutes, peaks at 30–60 minutes
- **Edibles:** Onset in 30–90 minutes, can last 4–8 hours
- **Tinctures:** Sublingual onset 15–30 minutes

### Individual Factors
- Body weight and metabolism
- Prior cannabis experience
- Genetics (some people metabolize THC differently)
- Current mood and environment ("set and setting")

---

## 5. THC Potency in Maryland Products

Maryland dispensary products must display THC content clearly:

- **Flower:** Listed as % (e.g., 20% THC)
- **Edibles:** Listed in mg per serving and per package
- **Concentrates:** Can be 60–90%+ THC

### Potency Comparison

| Product Type | Typical THC | Onset | Duration |
|---|---|---|---|
| Flower (joint) | 15-25% | 5-15 min | 2-4 hours |
| Vape cartridge | 70-90% | 5-10 min | 1-3 hours |
| Edible (10mg) | 10mg | 30-90 min | 4-8 hours |
| Concentrate (dab) | 60-90% | Immediate | 1-3 hours |

---

## 6. THC Safety Considerations

### Overconsumption
If you consume too much THC:
- Find a calm, safe environment
- Stay hydrated
- Remember: no one has died from THC overdose
- Effects will pass (usually within hours)
- CBD may help reduce intensity for some people

### Who Should Avoid THC
- Pregnant or breastfeeding individuals
- Those with history of psychosis or schizophrenia
- People on certain medications (consult doctor)
- Anyone under 21 (or under 18 for medical)

### Legal Reminders
- Never drive while impaired
- Don''t consume in public
- Keep products away from children and pets

