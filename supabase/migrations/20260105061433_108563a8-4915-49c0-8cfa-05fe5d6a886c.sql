## 6. Terpene Preservation

Terpenes are volatile and can degrade:

### Tips for Maintaining Terpenes
- Store cannabis in airtight containers
- Keep away from heat and light
- Avoid grinding until ready to use
- Lower temperature vaping preserves terpenes better

