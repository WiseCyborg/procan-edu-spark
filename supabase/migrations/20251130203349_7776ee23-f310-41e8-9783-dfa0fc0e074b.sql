## Overview
Proper dosage guidance is one of the most critical services dispensary employees provide to medical cannabis patients. Unlike traditional pharmaceuticals with standardized dosing, cannabis requires personalized titration based on individual factors including tolerance, medical conditions, body chemistry, and product type. This module covers evidence-based dosage principles, patient consultation techniques, and safety protocols for helping patients find their optimal therapeutic dose.

## Fundamental Dosage Principles

### "Start Low, Go Slow" Philosophy
The foundational approach to medical cannabis dosing:
- Begin with lowest effective dose (2.5-5mg THC for edibles)
- Wait sufficient time to assess effects (2-4 hours for edibles)
- Increase gradually in small increments (2.5-5mg at a time)
- Track effects in journal (dose, time, relief, duration)
- Stabilize at effective dose before further increases

**Why This Matters:**
Cannabis affects individuals differently due to:
- Endocannabinoid system variations
- Metabolism differences
- Tolerance levels
- Concurrent medications
- Body weight and composition

There is no "one size fits all" dose.

### Titration Process
Systematic dose-finding approach:
1. **Baseline establishment**: Start with minimal dose
2. **Effect observation**: Wait full duration before assessing
3. **Gradual escalation**: Small incremental increases
4. **Sweet spot identification**: Optimal relief with minimal side effects
5. **Dose stabilization**: Maintain effective dose for 1-2 weeks
6. **Reassessment**: Adjust as needed based on response

### Therapeutic Windows
Understanding dose-response relationships:
- **Microdose**: 1-2.5mg THC (sub-perceptual effects, daytime functionality)
- **Low dose**: 2.5-15mg THC (mild relief, minimal impairment)
- **Moderate dose**: 15-30mg THC (stronger effects, noticeable impairment)
- **High dose**: 30-50mg+ THC (intense effects, significant impairment)

Most patients find relief in low-to-moderate range.

## Product-Specific Dosing Recommendations

### Inhalation (Smoking/Vaping)
**Onset**: 5-10 minutes
**Peak**: 30-60 minutes
**Duration**: 2-4 hours

**Dosing guidance**:
- Start with 1-2 small inhalations
- Wait 15-20 minutes to assess effects
- Take additional small doses if needed
- Lower potency flower (10-15% THC) recommended for new patients
- Vaporization preferred over smoking for respiratory health

**Patient instructions**:
"Take one small puff, wait 15-20 minutes. If you need more relief, take another small puff. It''s easier to take more than to undo too much."

### Edibles
**Onset**: 30-90 minutes
**Peak**: 2-4 hours
**Duration**: 4-8 hours (sometimes longer)

**Dosing guidance**:
- Start with 2.5-5mg THC for new patients
- Wait minimum 2 hours before considering additional dose
- Effects can intensify over time (delayed peak)
- Food in stomach delays onset but prolongs effects
- Never "double up" if effects haven''t started yet

**Critical warning**:
"Edibles take 30-90 minutes to work. Do NOT take more if you don''t feel effects immediately. Overconsumption causes intense, prolonged discomfort."

### Tinctures/Oils
**Sublingual onset**: 15-30 minutes
**Oral onset**: 30-90 minutes
**Duration**: 4-6 hours

**Dosing guidance**:
- Start with 0.25-0.5mL (check mg per mL on label)
- Hold under tongue 30-60 seconds for sublingual absorption
- Swallowing provides slower, longer-lasting effects
- Precise dosing with dropper enables easy titration
- Can mix with food/beverage if taste unpleasant

### Topicals
**Onset**: 15-30 minutes
**Duration**: 2-4 hours

**Dosing guidance**:
- Apply liberally to affected area
- Non-intoxicating (does not enter bloodstream)
- Reapply as needed for localized relief
- Not suitable for systemic symptom relief

### Concentrates
**Onset**: Immediate (vaporized)
**Duration**: 1-3 hours

**Dosing guidance**:
- **NOT recommended for new patients** (extremely high potency)
- Start with "rice grain" sized amount
- Effects are intense and rapid
- Reserved for experienced patients with high tolerance
- Refer new patients to lower-potency options first

## Medical Condition Considerations

### Pain Management
Dosing approaches by pain type:
- **Acute pain**: Inhalation for rapid relief, titrate 1-2 puffs at a time
- **Chronic pain**: Combination approach (edible for baseline + inhalation for breakthrough)
- **Localized pain**: Topicals for targeted relief without systemic effects
- **Neuropathic pain**: Often requires higher doses, consider CBD:THC ratios

### Sleep Disorders
Recommended protocols:
- **Insomnia**: Indica-dominant edibles (5-15mg THC) 1-2 hours before bed
- **Sleep maintenance**: Longer-acting products to prevent middle-of-night waking
- **Morning grogginess**: Lower dose or earlier timing if residual effects occur
- **CBN content**: Cannabinol (CBN) shows promise for sleep support

### Anxiety and Mood
Careful dosing required:
- **Lower doses**: May reduce anxiety
- **Higher doses**: Can worsen anxiety or cause paranoia
- **CBD-rich products**: Often better tolerated for anxiety
- **THC:CBD ratios**: 1:1 or higher CBD content recommended
- **Avoid sativa-dominant strains**: May increase anxiety in sensitive patients

### Appetite Stimulation
Effective approaches:
- **Low doses**: Often sufficient to stimulate appetite
- **Timing**: 30-60 minutes before meals
- **Inhalation**: Rapid onset matches mealtime
- **THC-dominant products**: More effective than CBD for appetite

## Patient Consultation Techniques

### Comprehensive Patient Assessment
Essential questions before recommending doses:
1. "Have you used cannabis before?" (Establishes tolerance baseline)
2. "What symptoms are you treating?" (Determines product type)
3. "What time of day do you need relief?" (Affects product selection)
4. "Do you need to remain functional, or can you rest?" (Determines potency)
5. "What medications are you currently taking?" (Identifies interaction risks)
6. "Have you experienced any adverse effects from cannabis?" (Safety screening)

### Setting Realistic Expectations
Educate patients that:
- Cannabis is not a "cure-all" or "miracle drug"
- Finding optimal dose requires patience and experimentation
- Effects vary by product, batch, and individual factors
- Some trial and error is normal and expected
- Not all symptoms respond equally to cannabis
- Cannabis complements, not replaces, medical treatment

### Documenting Consultations
Maintain records of:
- Date of consultation
- Patient concerns and questions
- Products recommended and dosing guidance provided
- Warnings or contraindications discussed
- Follow-up plan
- Employee signature

Documentation protects dispensary and demonstrates quality care.

## Dosing Safety Protocols

### Overconsumption Management
If patient reports taking too much:
**Symptoms of overconsumption**:
- Severe anxiety or panic
- Rapid heartbeat
- Nausea or vomiting
- Dizziness or disorientation
- Paranoid thoughts

**Management guidance**:
- Reassure patient effects are temporary (will pass)
- Encourage hydration with water
- Rest in calm, quiet environment
- Distraction with TV, music, or conversation
- Avoid additional cannabis consumption
- Symptoms typically resolve in 4-8 hours
- Seek medical attention if severe distress or cardiovascular concerns

### When to Refer to Healthcare Provider
Consult physician for:
- Patients taking high-risk medications (blood thinners, immunosuppressants)
- Complex medical conditions requiring specialized guidance
- Children or adolescents (pediatric dosing requires medical supervision)
- Pregnant or breastfeeding patients
- Patients not achieving relief with appropriate dosing
- Adverse reactions requiring medical evaluation

### Red Flags Requiring Caution
Exercise extra care with patients who:
- Have history of substance abuse disorders
- Experience severe mental health conditions
- Take medications metabolized by CYP450 enzymes
- Have cardiovascular disease
- Are elderly with multiple medications
- Are new to cannabis and have anxiety about trying it

## Dosage Journal Recommendations
Encourage patients to track:
- Date and time of dose
- Product name and batch number
- Exact dose consumed (mg THC/CBD)
- Method of consumption
- Time to onset
- Symptom relief achieved (scale 1-10)
- Side effects experienced
- Duration of effects
- Notes on effectiveness

Journals enable informed dose adjustments and product selection.

## Conclusion
Dosage guidance is a combination of science, experience, and individualized care. The "start low, go slow" approach protects patients from adverse experiences while systematically identifying their optimal therapeutic dose. Never guess at dosing recommendations—provide evidence-based guidance, set realistic expectations, and empower patients to actively participate in their titration process. When in doubt about appropriate dosing for a patient''s situation, recommend they consult their healthcare provider. Your role is educator and advisor, not physician.' 
