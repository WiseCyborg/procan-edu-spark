## 1. What Is the Endocannabinoid System?

The endocannabinoid system (ECS) is a natural system in your body that helps maintain balance—also known as **homeostasis**.

It plays a role in regulating:

- **Mood**
- **Sleep**
- **Appetite**
- **Pain perception**
- **Stress response**
- **Memory and focus**

Your body produces its own cannabinoids (called endocannabinoids) even if you never consume cannabis.

> **Key idea:** Cannabis doesn''t create a new system in your body — it interacts with one that already exists.

---

## 2. The Three Core Parts of the ECS

### A. Endocannabinoids (made by your body)

- **Anandamide (AEA)** – often linked to mood and stress
- **2-AG** – involved in immune response and inflammation

These act like internal messengers, released only when needed.

### B. Cannabinoid Receptors (the "locks")

**CB1 Receptors**
- Found mainly in the brain and nervous system
- Affect mood, memory, coordination, perception
- THC interacts strongly with CB1

**CB2 Receptors**
- Found mainly in the immune system and body tissues
- Affect inflammation, pain, and immune response
- CBD interacts more indirectly here

### C. Enzymes (the cleanup crew)

Once cannabinoids do their job, enzymes break them down so the system doesn''t stay overstimulated.

---

## 3. How THC and CBD Interact with the ECS

### THC (Tetrahydrocannabinol)

- Binds directly to CB1 receptors
- Produces intoxicating effects ("high")
- Can affect perception, coordination, anxiety, and focus
- Effects vary widely by dose and individual

### CBD (Cannabidiol)

- Does not directly bind to CB1
- Modulates how receptors respond
- Often associated with calming or balancing effects
- Can reduce some THC side effects for certain people

> **Important:** CBD does not cancel THC — it can influence how THC feels, but effects vary.

---

## 4. Why Cannabis Affects Everyone Differently

There is no "standard" cannabis experience.

Differences come from:

- Individual ECS sensitivity
- Body chemistry and metabolism
- Tolerance
- Product type (flower, edible, vape)
- THC/CBD ratio
- Method of consumption
- Dose

This is why responsible consumption guidance emphasizes:

**Start low. Go slow. Observe your response.**

---

## 5. The ECS and Responsible Consumption

Understanding the ECS helps explain:

- Why overconsumption can cause discomfort
- Why edibles feel stronger or last longer
- Why waiting between doses matters
- Why mixing substances can be risky

> **Safety takeaway:** Cannabis affects systems related to judgment, coordination, and reaction time. Avoid driving or operating machinery while impaired.

